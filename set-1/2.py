# 2. **Data Type Play**: Create variables of each data type (integer, float, string, boolean, list, tuple, dictionary, set) and print their types and values.
#     - *Input*: None
#     - *Output*: "Type of variable1: <class 'int'>, value: 10..."

# Integer
my_int = 10

# Float
my_float = 3.14

# String
my_string = "Hello, World!"

# Boolean
my_boolean = True

# List
my_list = [1, 2, 3, 4, 5]

# Tuple
my_tuple = (6, 7, 8, 9, 10)

# Dictionary
my_dict = {"name": "John", "age": 25}

# Set
my_set = {1, 2, 3, 4, 5}

# Printing types and values
print("Type of my_int:", type(my_int), ", value:", my_int)
print("Type of my_float:", type(my_float), ", value:", my_float)
print("Type of my_string:", type(my_string), ", value:", my_string)
print("Type of my_boolean:", type(my_boolean), ", value:", my_boolean)
print("Type of my_list:", type(my_list), ", value:", my_list)
print("Type of my_tuple:", type(my_tuple), ", value:", my_tuple)
print("Type of my_dict:", type(my_dict), ", value:", my_dict)
print("Type of my_set:", type(my_set), ", value:", my_set)

