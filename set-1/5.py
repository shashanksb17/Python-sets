# 5. **String Reversal**: Write a Python function that takes a string and returns the string in reverse order.
#     - *Input*: "Python"
#     - *Output*: "nohtyP"

def reverse_string(input_string):
    return input_string[::-1]

# Example usage
input_str = "Python"
reversed_str = reverse_string(input_str)
print(reversed_str)


# nohtyP
