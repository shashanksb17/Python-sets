# 4. **Sum and Average**: Write a Python program that calculates and prints the sum and average of a list of numbers.
#     - *Input*: [10, 20, 30, 40]
#     - *Output*: "Sum: 100, Average: 25.0"

numbers = [10, 20, 30, 40]

# Calculate the sum
sum_of_numbers = sum(numbers)

# Calculate the average
average_of_numbers = sum_of_numbers / len(numbers)

# Print the sum and average
print("Sum:", sum_of_numbers)
print("Average:", average_of_numbers)

# Sum: 100
# Average: 25.0
